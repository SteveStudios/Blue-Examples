module.exports = class MathTools {
    static one = 1;
    
    static arcsine(x) {
        return Math.asin(x);
    }
    
    static arccos(x) {
        return Math.acos(x);
    }
    
    static cosine(x) {
        return Math.cos(x);
    }
    
    static sine(x) {
        return Math.sin(x);
    }
    
    static floorValue(x) {
        return Math.floor(x);
    }
    
}